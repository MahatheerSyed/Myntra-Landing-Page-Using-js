//create cards
function createCards(
  title,
  subTitle,
  price,
  removedPrice,
  discount,
  image_url
) {
  let cards_container = document.querySelector(".cards-container");
  let detailsObj = {
    title: title,
    subTitle: subTitle,
    price: price,
    removedPrice: removedPrice,
    discount: discount,
    image_url: image_url,
  };
  if (detailsObj) {
    //create card element
    let card = document.createElement("div");
    card.classList.add("card");
    let item_img = document.createElement("div");
    item_img.classList.add("item-img");
    //creating image and set url
    let image = document.createElement("img");
    image.src = detailsObj.image_url;
    image.alt = detailsObj.title;
    item_img.appendChild(image);
    card.appendChild(item_img);
    let item_data = document.createElement("div");
    item_data.classList.add("item-data");
    //create title tag
    let title_elem = document.createElement("h1");
    title_elem.innerText = detailsObj.title;
    item_data.appendChild(title_elem);
    //creating subtitle tag
    let subTitle_elem = document.createElement("h2");
    subTitle_elem.innerText = detailsObj.subTitle;
    item_data.appendChild(subTitle_elem);
    //creating price tag
    let price_elem = document.createElement("h3");
    price_elem.innerText = detailsObj.price;
    //creating discount elment
    let discount_elem = document.createElement("span");
    discount_elem.id = "removedprice";
    discount_elem.innerText = detailsObj.discount;
    price_elem.appendChild(discount_elem);
    //creating removed price
    let removedPrice_elem = document.createElement("span");
    removedPrice_elem.id = "discount";
    removedPrice_elem.innerText = detailsObj.removedPrice;
    price_elem.appendChild(removedPrice_elem);

    item_data.appendChild(price_elem);
    card.appendChild(item_data);
    cards_container.appendChild(card);
    // Display();
  }
}

let Display = (myntraCardDetails) => {
  document.querySelector(".cards-container").innerHTML = "";
  console.log(myntraCardDetails);
  if (myntraCardDetails) {
    myntraCardDetails.forEach((index) => {
      createCards(
        index.title,
        index.subTitle,
        index.price,
        index.removedPrice,
        index.discount,
        index.image_url
      );
    });
  }
};

Display(myntraCardDetails);
